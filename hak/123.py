import csv

def basesearch(file_path, query):
    try:
        # Определяем тип файла по расширению
        if file_path.endswith('.txt'):
            with open(file_path, 'r', encoding='utf-8') as file:
                results = []
                for line in file:
                    if query.lower() in line.lower():  # Поиск без учета регистра
                        results.append(line.strip())
        
        elif file_path.endswith('.csv'):
            with open(file_path, 'r', encoding='utf-8') as file:
                reader = csv.reader(file)
                results = []
                for row in reader:
                    if any(query.lower() in cell.lower() for cell in row):  # Поиск в любой ячейке
                        results.append(row)
        
        else:
            print("Поддерживаются только .txt и .csv файлы.")
            return

        # Обработка результатов
        if results:
            for result in results:
                print(result)
        else:
            print("Ничего не найдено.")
    
    except Exception as e:
        print(f"Ошибка при выполнении поиска: {e}")

import phonenumbers
from phonenumbers import carrier, geocoder, timezone

def phoneinfo(phone):
    """Получить информацию о номере телефона."""
    try:
        parsed_phone = phonenumbers.parse(phone, None)
        
        if not phonenumbers.is_valid_number(parsed_phone):
            return "\n[!] Произошла ошибка -> Недействительный номер телефона\n"

        carrier_info = carrier.name_for_number(parsed_phone, "en") or "Неизвестный оператор"
        country = geocoder.description_for_number(parsed_phone, "en") or "Неизвестная страна"
        region = geocoder.description_for_number(parsed_phone, "ru") or "Неизвестный регион"
        formatted_number = phonenumbers.format_number(parsed_phone, phonenumbers.PhoneNumberFormat.INTERNATIONAL)
        is_valid = phonenumbers.is_valid_number(parsed_phone)
        is_possible = phonenumbers.is_possible_number(parsed_phone)
        timezones = timezone.time_zones_for_number(parsed_phone)

        print_phone_info = f"""\n[+] Номер телефона -> {formatted_number}
    [+] Страна -> {country}
    [+] Регион -> {region}
    [+] Оператор -> {carrier_info}
    [+] Активен -> {is_possible}
    [+] Валид -> {is_valid}
    [+] Таймзона -> {timezones}
    [+] Telegram -> https://t.me/{phone}
    [+] Whatsapp -> https://wa.me/{phone}
    [+] Viber -> https://viber.click/{phone}\n"""

        return print_phone_info

    except phonenumbers.NumberParseException:
        return "\n[!] Произошла ошибка -> Неверный формат номера телефона\n"
    except Exception as e:
        return f"\n[!] Произошла ошибка -> {str(e)}\n"

