    def luhn_check(num):
        """Проверка номера карты по алгоритму Луна."""
        num = [int(d) for d in str(num)]
        checksum = 0
        # Удвоение каждого второго числа с конца
        for i in range(len(num) - 2, -1, -2):
            num[i] *= 2
            if num[i] > 9:
                num[i] -= 9
        checksum = sum(num)
        return checksum % 10 == 0

